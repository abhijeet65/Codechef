inline void fastRead(int *a)
{
	register char c=0;
	while (c<33) c=getchar_unlocked();
	*a=0;
	while (c>33)
	{
	    *a=*a*10+c-'0';
	    c=getchar_unlocked();
	}
}
double root(int a,int b,int c)
{
	double d=b*b-4.0*a*c;
	if(d==0)
		return b/(-2.0)*a;
	else if(d>0)
		return max(-b+sqrt(d),-b-sqrt(b))/(2.0*a);
	return 0;
}
